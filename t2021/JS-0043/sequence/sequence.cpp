template<typename T> ostream& operator<<(ostream& os,vector<T> v){
	os<<"[";
	for(auto x:v){
		os<<x<<" ";
	}
	os<<"]";
	return os;
}

int n,m,k;
ll v[105];
ll fact[35];
ll invfact[35];
ll power[105][35];
ll dp[35][35][105][35];

const ll mod=998244353;

ll qp(ll a,ll b){
	if(b==0){
		return 1;
	}
	ll ans=qp(a,b/2);
	if(b%2){
		return ans*ans%mod*a%mod;
	}else{
		return ans*ans%mod;
	}
}

inline void ADD(ll& a,ll b){
	a+=b;
	if(a>=mod){
		a-=mod;
	}
}

ll dfs(int nowA,int oneCnt,int nowV,int carrier){
	if(nowA==n){
		if(__builtin_popcount(carrier)+oneCnt>k){
			return 0;
		}else{
			return fact[n];
		}
	}
	
	if(nowV==m+1){
		return 0;
	}
	
	ll& zjs=dp[nowA][oneCnt][nowV][carrier];
	if(zjs!=-1){
		return zjs;
	}
	
	ll ans=0;
	for(int i=0;i+nowA<=n;i++){ //place how many of current digit
		if(oneCnt+(i+carrier)%2<=k){
			ADD(ans,
				dfs(nowA+i,oneCnt+(i+carrier)%2,nowV+1,(i+carrier)/2)*power[nowV][i]%mod*invfact[i]%mod
			);
		}
	}
	
//	cout<<nowA<<" "<<oneCnt<<" "<<nowV<<" "<<carrier<<"="<<ans<<endl;
	return zjs=ans;
}

int main(){
	freopen("sequence.in","r",stdin);
	#ifndef XGN
	freopen("sequence.out","w",stdout);
	#endif
	
	memset(dp,-1,sizeof(dp));
	
	fact[0]=1;
	invfact[0]=1;
	for(int i=1;i<=33;i++){
		fact[i]=1ll*fact[i-1]*i%mod;
		invfact[i]=qp(fact[i],mod-2);
	}
	
	cin>>n>>m>>k;
	for(int i=0;i<m+1;i++){
		cin>>v[i];
		
		power[i][0]=1;
		for(int j=1;j<=33;j++){
			power[i][j]=power[i][j-1]*v[i]%mod;
//			cout<<power[i][j]<<" ";
		}
//		cout<<endl;
	}
	
	cout<<dfs(0,0,0,0)<<endl;
	return 0;
}
